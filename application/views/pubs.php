  <h1 class="head1">Publications & Ethics</h1>
  <hr>
  <h3 class="subpube">Publications</h3>
	<div class="row" style="font-family:'Roboto'; text-align: justify">
		Section A: Publication and authorship</p><ol><li>
<p style="margin-bottom: 0in">All submitted papers are subject
	to strict peer-review process by at least two international
	reviewers that are experts in the area of the particular paper. 
	</p>
	</li><li>
<p style="margin-bottom: 0in">The factors that are taken into
	account in review are relevance, soundness, significance,
	originality, readability and language. 
	</p>
	</li><li>
<p style="margin-bottom: 0in">The possible decisions include
	acceptance, acceptance with revisions, or rejection. 
	</p>
	</li><li>
<p style="margin-bottom: 0in">If authors are encouraged to
	revise and resubmit a submission, there is no guarantee that the
	revised submission will be accepted. 
	</p>
	</li><li>
<p style="margin-bottom: 0in">Rejected articles will not be
	re-reviewed. 
	</p>
	</li><li>
<p style="margin-bottom: 0in">The paper acceptance is
	constrained by such legal requirements as shall then be in force
	regarding libel, copyright infringement and plagiarism. 
	</p>
	</li>
	<li>
<p style="margin-bottom: 0in">No research can be included in more than one publication. 
	</p>
</li>
	<li>
<p>All accepted papers will be published in <b>IEEE XPLORE and international journals</b>. 
	</p><p><br></p>
</li>
	<h3 class="subpube" style="margin-left:-40px;">Ethics</h3>
</ol><p><a name="2"></a>Section B: Authors’ responsibilities</p><ol><li>
<p style="margin-bottom: 0in">Authors must certify that their
	manuscripts are their original work. 
	</p>
	</li><li>
<p style="margin-bottom: 0in">Authors must certify that the
	manuscript has not previously been published elsewhere. 
	</p>
	</li><li>
<p style="margin-bottom: 0in">Authors must certify that the
	manuscript is not currently being considered for publication
	elsewhere. 
	</p>
	</li><li>
<p style="margin-bottom: 0in">Authors must participate in the
	peer review process. 
	</p>
	</li><li>
<p style="margin-bottom: 0in">Authors are obliged to provide
	retractions or corrections of mistakes. 
	</p>
	</li><li>
<p style="margin-bottom: 0in">All Authors mentioned in the
	paper must have significantly contributed to the research. 
	</p>
	</li><li>
<p style="margin-bottom: 0in">Authors must state that all data
	in the paper are real and authentic. 
	</p>
	</li><li>
<p style="margin-bottom: 0in">Authors must notify the Editors
	of any conflicts of interest. 
	</p>
	</li><li>
<p style="margin-bottom: 0in">Authors must identify all
	sources used in the creation of their manuscript. 
	</p>
	</li><li>
<p>Authors must report any errors they discover in their
	published paper to the Editors. 
	</p><p><br></p>
</li></ol><p><a name="3"></a>Section C: Reviewers’ responsibilities</p><ol><li>
<p style="margin-bottom: 0in">Reviewers should keep all
	information regarding papers confidential and treat them as
	privileged information. 
	</p>
	</li><li>
<p style="margin-bottom: 0in">Reviews should be conducted
	objectively, with no personal criticism of the author 
	</p>
	</li><li>
<p style="margin-bottom: 0in">Reviewers should express their
	views clearly with supporting arguments 
	</p>
	</li><li>
<p style="margin-bottom: 0in">Reviewers should identify
	relevant published work that has not been cited by the authors. 
	</p>
	</li><li>
<p style="margin-bottom: 0in">Reviewers should also call to
	the Editor in Chief’s attention any substantial similarity or
	overlap between the manuscript under consideration and any other
	published paper of which they have personal knowledge. 
	</p>
	</li><li>
<p>Reviewers should not review manuscripts in which they have
	conflicts of interest resulting from competitive, collaborative, or
	other relationships or connections with any of the authors,
	companies, or institutions connected to the papers. 
	</p><p><br></p>
</li></ol><p><a name="4"></a>Section D: Editors’ responsibilities</p><ol><li>
<p style="margin-bottom: 0in">Editors have complete
	responsibility and authority to reject/accept an article. 
	</p>
	</li><li>
<p style="margin-bottom: 0in">Editors are responsible for the
	contents and overall quality of the publication. 
	</p>
	</li><li>
<p style="margin-bottom: 0in">Editors should always consider
	the needs of the authors and the readers when attempting to improve
	the publication. 
	</p>
	</li><li>
<p style="margin-bottom: 0in">Editors should guarantee the
	quality of the papers and the integrity of the academic record. 
	</p>
	</li><li>
<p style="margin-bottom: 0in">Editors should publish errata
	pages or make corrections when needed. 
	</p>
	</li><li>
<p style="margin-bottom: 0in">Editors should have a clear
	picture of a research’s funding sources. 
	</p>
	</li><li>
<p style="margin-bottom: 0in">Editors should base their
	decisions solely one the papers’ importance, originality, clarity
	and relevance to publication’s scope. 
	</p>
	</li><li>
<p style="margin-bottom: 0in">Editors should not reverse their
	decisions nor overturn the ones of previous editors without serious
	reason. 
	</p>
	</li><li>
<p style="margin-bottom: 0in">Editors should preserve the
	anonymity of reviewers. 
	</p>
	</li><li>
<p style="margin-bottom: 0in">Editors should ensure that all
	research material they publish conforms to internationally accepted
	ethical guidelines. 
	</p>
	</li><li>
<p style="margin-bottom: 0in">Edittors should only accept a
	paper when reasonably certain. 
	</p>
	</li><li>
<p style="margin-bottom: 0in">Editors should act if they
	suspect misconduct, whether a paper is published or unpublished, and
	make all reasonable attempts to persist in obtaining a resolution to
	the problem. 
	</p>
	</li><li>
<p style="margin-bottom: 0in">Editors should not reject papers
	based on suspicions, they should have proof of misconduct. 
	</p>
	</li><li>
<p>Editors should not allow any conflicts of interest between
	staff, authors, reviewers and board members.</p></li></ol>
