  <h1 class="head1">Visa Matters</h1>
  <hr>
	<div class="row" style="font-family:'Roboto'; margin-left: 10px; text-align: justify">
		<p>
		In accordance with the Regulation of Minister of Law and Human Right of the Republic of Indonesia Number 26, 2013, citizen from 61 countries and 1 region are eligible for obtaining <b>Visa on Arrival (VOA)</b>. <br><br>
		This visa can be obtained directly when you are landed at the certain airports and seaport in Indonesia regardless of the purpose of your visits (Business, Tourist, Social). <b>The Visa on Arrival is not a work visa nor a visitation visa. Therefore, it can not be converted to obtain othe immigration permits</b>. The maximun stays permitted for the visa on arrival is 30 days. If you plan to stay longer than 30 days, you need to mention your intention to stay longer. Visa on Arrival can be extended for another 30 days.
		</p>
		<br>
		The general requirements for <b>Visa On Arrival</b> are:
		<ul>
			<li>Expiration date of the applicant's [passport must be at least 6 (six) months at the date of entry</li>
			<li>At least one blank visa page</li>
			<li>Round-trip airplane ticket</li>
			<li><b>Visa on Arrival fee (refer below)</b></li>
		</ul>
		<br>
		The fees for <b>Visa On Arrival</b> are:
		<ul>
			<li>Visa for stay up to 7 (seven) days in several Special Economic Zones (SEZ) = 15 USD</li>
			<li>Visa for stay up to 30 (thirty) days = US$ 35,-</li>
			<li>Extendsion of stay for up to 30 (thirty) days = US$ 35,-</li>
		</ul>

		<p>Source : <a href="http://www.embassyofindonesia.org/" title="">http://www.embassyofindonesia.org/</a></p>
